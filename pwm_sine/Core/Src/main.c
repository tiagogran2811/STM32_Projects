uint16_t sine_lut[LUT_SIZE];		// Sine lookup table (stores duty cycle values)
uint8_t rx_data;                   	// Single received byte
char rx_buffer[RX_BUFFER_SIZE];    	// Buffer to store received string
uint8_t rx_index = 0;              	// Current position in buffer
volatile float step = 1.0;         	// Step size controls output frequency
float index_f = 0;                 	// Floating index for LUT traversal


void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
    if (huart->Instance == USART1)
    {
        char c = rx_data;

        if (c == '\n' || c == '\r')		// Check for end of command (newline or carriage return)
        {
            if (rx_index > 0)
            {
            	rx_buffer[rx_index] = '\0';			// Null-terminate string
            	float freq = atof(rx_buffer);		// Convert received string to float (frequency)

            	// Clamp frequency between 20 Hz and 200 Hz
                if (freq < 20.0) freq = 20.0;
                if (freq > 200.0) freq = 200.0;

                step = (freq * LUT_SIZE) / F_UPDATE; // Calculate DDS step based on desired frequency

            }
            // Reset buffer for next command
            rx_index = 0;
            memset(rx_buffer, 0, RX_BUFFER_SIZE);
        }
        else
        {
        	// Store received character if buffer is not full
            if (rx_index < RX_BUFFER_SIZE - 1)
            {
                rx_buffer[rx_index++] = c;
            }
        }

        HAL_UART_Receive_IT(&huart1, &rx_data, 1);
    }
}


void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
    if (htim->Instance == TIM3)
    {
    	// Increment LUT index using DDS step
    	index_f += step;

    	// Wrap around LUT index
    	while (index_f >= LUT_SIZE) index_f -= LUT_SIZE;

    	// Update PWM duty cycle using LUT value
    	__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, sine_lut[(uint16_t)index_f]);
    }
}
